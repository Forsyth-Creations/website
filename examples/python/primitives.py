# Defining primitives in Python

# Integer
age = 25

# Float
temperature = 98.6

# String
name = "Alice"

# Boolean
is_active = True

# None
value = None

# List
numbers = [1, 2, 3, 4, 5]

# Tuple
coordinates = (10.0, 20.0)

# Dictionary
person = {"name": "Bob", "age": 30}

print(f"Name: {name}, Age: {age}, Active: {is_active}")
